from customtkinter import CTkFrame
from PackArgs import PackArgs


class Frame(CTkFrame, PackArgs):
    def __init__(self, *args, properties, **kwargs):
        super().__init__(*args, **kwargs)
        self.type = "FRAME"
        self.properties = properties
        self.pack_options = {}
        self.pack_propagate(False)
        self.configure(bg_color=self.master.master.cget("fg_color"))

        self.order = 0
        #self.bind("<B1-Motion>", self.on_drag_motion)
        self.props = {}



    def get_name(self):
        return f"{self.type} {str(self.order)}"

    def save(self, func, key, val, arg):
        self.props[key] = val
        func(arg)

    def configure(self, require_redraw=False, **kwargs):
        print(kwargs)
        super().configure(require_redraw, **kwargs)


    def _bool_change(self, val):
        if val == "True":
            return True
        elif val == "False":
            return False

